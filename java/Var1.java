public class Var1 {
	public static void main(String[] args) {
		boolean bool1 = false;
//		정수 : 0, +, - 를 포함하는 소수점 없는 숫자.
		int a = 1;
		char ch = 'Z';
//		실수 : 0, +, - 를 포함하는 소수점 없는 숫자.
		float f = 0.123456789f;//소수점 7자리까지 신뢰
		double d = 0.12345678901234567890d;//소수점 16자리까지 신뢰
		
		System.out.println(f);
		System.out.println(d);
	}

}
