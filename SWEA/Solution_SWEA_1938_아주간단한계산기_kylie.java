
import java.util.Scanner;

public class Solution_SWEA_1938_아주간단한계산기_kylie {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();			
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
	}//end of main
}//end of class